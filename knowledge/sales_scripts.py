def get_script_by_scene(scene: str) -> dict:
    """根据场景获取话术"""
    return SALES_SCRIPTS.get(scene, {})

def get_script_by_keyword(keyword: str) -> dict:
    """根据关键词匹配场景"""
    keyword = keyword.lower()
    for scene, keywords in SCENE_KEYWORDS.items():
        for kw in keywords:
            if kw in keyword:
                return SALES_SCRIPTS.get(scene, {})
    return {}

def get_all_scenes() -> list:
    """获取所有场景"""
    return list(SALES_SCRIPTS.keys())
